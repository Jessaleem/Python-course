#Pedir un número a un usuario, la información ingresada la toma tipo string
numero = input('Escribe un número: ')

#Multiplicar el número por 2, se debe convertir a entero (int)
resultado_entero = int(numero) * 2

#convertir a flotante (float) y multiplicar por 2
resultado_flotante = float(numero) * 2

#Mostrar el resultado
print(resultado_flotante)